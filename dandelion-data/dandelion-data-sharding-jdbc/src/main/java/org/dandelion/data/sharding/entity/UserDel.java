        sb.append(", phone=").append(phone);
        sb.append(", age=").append(age);
        sb.append(", remark=").append(remark);
        sb.append(", address=").append(address);
        sb.append(", serialVersionUID=").append(serialVersionUID);
        sb.append("]");
        return sb.toString();
